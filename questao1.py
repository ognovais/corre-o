a = '5'
b = 2
c = a * b
print(c)

# Relatório:
# Saída obtida: 55
# Resposta correta: 55
# Resposta marcada: 10
# Análise dos erros: O erro ocorreu porque a variável 'a' é uma string. A multiplicação de uma string por um número resulta na repetição da string, não na multiplicação numérica. Para obter 10, 'a' deveria ser um número (inteiro ou float).
